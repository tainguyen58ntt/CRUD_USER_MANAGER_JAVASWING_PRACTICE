    private void initTable() {
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"User name", "Password", "Role"});
        tblUsers.setModel(tblModel);
    }

    private void fillTable() {
        while (tblModel.getRowCount() > 0) {
            tblModel.removeRow(0);
        }
        for (User user : list) {
            tblModel.addRow(new String[]{user.getUserName(), user.getPassWord(), user.getRole()});
        }

        tblModel.fireTableDataChanged();
    }

    private void initData() {
        list.add(new User("U01", "abc", "User"));
        list.add(new User("U02", "abc", "User"));
        list.add(new User("U03", "abc", "Admin"));
        list.add(new User("U04", "abc", "User"));
    }

