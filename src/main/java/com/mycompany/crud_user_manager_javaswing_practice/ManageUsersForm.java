    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        StringBuilder builder = new StringBuilder();
        if (txtUserName.getText().equals("")) {
            builder.append("Username not empty !!! \n");

        }
        if (builder.length() > 0) {
            JOptionPane.showMessageDialog(this, builder.toString());
            return;
        }
        for (User user : list) {
            if (user.getUserName().equals(txtUserName.getText())) {
                int choice = JOptionPane.showConfirmDialog(this, "Are u sure ?", "Confirm", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {

                    list.remove(user);
                    fillTable();
                    JOptionPane.showMessageDialog(this, "User have been removed");
                    btnResetActionPerformed(evt);
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "User not found");


    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        StringBuilder builder = new StringBuilder();
        if (txtUserName.getText().equals("")) {
            builder.append("Username not empty !!! \n");

        }
        if (builder.length() > 0) {
            JOptionPane.showMessageDialog(this, builder.toString());
            return;
        }
        for (User user : list) {
            if(user.getUserName().equals(txtUserName.getText())){
                user.setPassWord(new String(txtPassWord.getPassword()));
                user.setRole(rdbAdmin.isSelected() ? "Admin" : "User");
                
                fillTable();
                JOptionPane.showMessageDialog(this, "Update success");
                btnResetActionPerformed(evt);
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "the user can't found");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        StringBuilder builder = new StringBuilder();
        if (txtUserName.getText().equals("")) {
            builder.append("Username not empty !!! \n");

        }
        if (builder.length() > 0) {
            JOptionPane.showMessageDialog(this, builder.toString());
            return;
        }
//        list.add(new User(txtUserName., passWord, role)

        String userName = txtUserName.getText();
        String passWord = new String(txtPassWord.getPassword());
        String role = rdbAdmin.isSelected() ? "Admin" : "User";
        list.add(new User(userName, passWord, role));
        fillTable();
        JOptionPane.showMessageDialog(this, "add new success");
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_btnAddActionPerformed

